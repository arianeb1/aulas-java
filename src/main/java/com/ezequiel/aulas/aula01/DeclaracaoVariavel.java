package com.ezequiel.aulas.aula01;

public class DeclaracaoVariavel {

	public static void main(String[] args) {
		//para declarar uma variavel, é necessário informar o tipo e nome da variavel
		int variavelInt;
		//setar valor na variavel
		variavelInt = 10;
		//pode-se declarar mais de uma variavel do mesmo tipo separada por virgula
		char a,b,c,d;
		//uma variavel pode ser declarada ja com o seu valor definido
		double db1 = 50.23;
		//pode-ser declarar mais de uma variavel do mesmo tipo separada por virgula e com valores pre-definidos
		int e=1,f=2,g=4,h,i,j;
		
		/*
		 * nomes de variaveis não podem ser paralavras reservadas do java.
		 *   int double = 56
		 *   a linha acima não compila pois "double" é uma palavra reservada do java
		 *   para conhecer quais são as paravras reservadas, ver artigo:  
		 *   http://www.linhadecodigo.com.br/artigo/83/as-52-palavras-reservadas-do-java.aspx
		 */
		 
	}
}
